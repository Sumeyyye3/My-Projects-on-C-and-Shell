int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] <= 57 && str[i] >= 48))
		{
			i++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
