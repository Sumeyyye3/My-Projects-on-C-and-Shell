#include<unistd.h>

void	ft_putnbr(int nb)
{
	char	nb_c;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb *= -1;
		}
		if (nb >= 10)
		{
			ft_putnbr(nb / 10);
		}
		nb = nb % 10;
		nb_c = nb + '0';
		write(1, &nb_c, 1);
	}
}
